std_grades=int(input("enter the grade:"))
if std_grades>=90:
    print("Grade:A")
elif std_grades>=80:
    print("Grade:B")
elif std_grades>=70:
    print("Grade:C")
elif std_grades>=60:
    print("Grade:D")
elif std_grades>=50:
    print("Grade:E")
else:
    print("Grade:F")